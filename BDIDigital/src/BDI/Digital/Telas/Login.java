    private void loginA () {
        //String de consulta ao banco com 2 Statemens (?)
        String sql = "SELECT * FROM USUARIO WHERE login = ? AND senha = ?";
        //Apenas executará a consulta se nenhum dos campos forem vazios
        if(!txtUsuLogin.getText().isEmpty() && !txtUsuSenha.getText().isEmpty()){
            try {
                //Prepara a consulta ao banco em cima da String sql
                pst = conexao.prepareStatement(sql);
                //Define o primeiro Statemente (?) da consulta
                pst.setString(1,txtUsuLogin.getText());
                //Define o primeiro Statemente (?) da consulta
                pst.setString (2,txtUsuSenha.getText());
                //Executa a consulta
                rs = pst.executeQuery();
                if (rs.next()){
                    //Se a consulta encontrar resultados mostra mensagem e inicia nova tela
                    JOptionPane.showMessageDialog(null, "Bem vindo " + rs.getString(2) + "!");
                    TelaPrincipal telap = new TelaPrincipal();
                    telap.setVisible(true);
                    telap.setLocationRelativeTo(null);
                    telap.usuarioLogado = rs.getString(1);
                    telap.setTitle("BDI Digital - Auto Viação Goianésia Ltda - Operador: " + rs.getString(2));
                    this.dispose();
                }
                else {
                    //Se a consulta não retornar resultados, mostra mensagem de erro 
                    JOptionPane.showMessageDialog(null, "Login e/ou Senha incorreto(s)");
                }
            } catch (Exception e) {
                //Se a consulta não puder ser realizada por qualquer exceção, exibe a exceção
                JOptionPane.showMessageDialog(null, e);
            }
        }
        else{
            //Exibe mensagem de campos obrigatórios ao tentar logar sem preencher todos os campos
            JOptionPane.showMessageDialog(null, "Todos os campos são obrigatórios");
        }
    }

